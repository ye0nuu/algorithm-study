# ✅ 내 풀이 아이디어:
# - 체스 총 개수 저장한 배열 생성
# - 발견한 개수 입력받아 분리하고 정수 변환 뒤 리스트로
# - 기존 개수에서 발견 개수 빼서 출력

chess = [1, 1, 2, 2, 2, 8]
found = list(map(int, input().split()))

for i in range(6):
  print(chess[i]-found[i], end=" ")